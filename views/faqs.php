<h4 id="E155">
    What's all this about?
</h4>
<p id="E158">
    Glipple is the place where free music lives. It’s a place for new-music enthusiasts to discover and interact with music freely and legally, and for artists
    to distribute their music and grow their fanbases without all the red tape.
</p>
<h4 id="E160">
    How is Glipple useful for artists and non-artists, respectively?
</h4>
<p id="E163">
    At Glipple, we cater to those looking for easy consumption of music, as well as those looking to easily disseminate their music amongst eager listeners.
    Therefore, Glipple has two main user functions: 1) artist, and 2) general user.
</p>
<p id="E165">
    Artists are free to upload music, garner radio play on Glipple’s radio, and manage profiles through a basic username/password signup, all while retaining
    full creative control over their works.
</p>
<p id="E167">
    General users (“non-artists”) are able to stream and download music, listen to Glipple’s radio, view and interact with artists’ profiles, and discover new
    material all for free, and all legally. General users are not required to submit either a username or password to stream/download from, view, or otherwise
    participate in the Glipple platform.
</p>
<p id="E175">
    We built Glipple so you can have full free and legal access to all the music you discover, so dig in!
</p>
<h4 id="E177">
    I’m an artist, so…
</h4>
<h4 id="E180">
    How do I submit my music?
</h4>
<p id="E183">
    After registering and logging in, follow the basic instructions to upload some cool music, providing song title(s), album(s), genre(s), and album artwork.
    Round out your artist profile by posting a basic bio, profile picture, and your contact details. Each song you upload should ideally include the
    aforementioned details.
</p>
<p id="E190">
    Once your music is uploaded to our database, it’s available for free stream and download but tons of listeners. Additionally, it becomes immediately
    eligible for play on Glipple’s radio, so the more people you get to stream and download, the more radio play you could get!
</p>
